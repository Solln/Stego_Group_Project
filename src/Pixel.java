public class Pixel {

int redValue;
int greenValue;
int blueValue;


public Pixel(int red, int green, int blue){
    this.redValue=red;
    this.greenValue=green;
    this.blueValue=blue;
}

    public int getRedValue() {
        return redValue;
    }

    public int getGreenValue() {
        return greenValue;
    }

    public int getBlueValue() {
        return blueValue;
    }

}
